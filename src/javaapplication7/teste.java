    public void CarregaTabela(){
        Controller objeto = new Controller();
        
        DefaultTableModel val = (DefaultTableModel) Tabela.getModel();
        
        
        for (int i = 0; i < objeto.getTotalTimes(); i++) {
            val.addRow(new Object[]{objeto.timeController.get(i).getNameTeam(),objeto.timeController.get(i).getTotalJogos(),
                                   objeto.timeController.get(i).getVitoria(), objeto.timeController.get(i).getEmpate(), 
                                objeto.timeController.get(i).getDerrota(), objeto.timeController.get(i).getTotalPontos(),
                            objeto.timeController.get(i).getGolsMarcados(), objeto.timeController.get(i).getGolsSofridos(), 
                        objeto.timeController.get(i).getSaldoGols()});
        }
        
    }
